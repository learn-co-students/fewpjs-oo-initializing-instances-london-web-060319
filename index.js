class Breakfast {
    constructor(eggs, juice) {
    this.food = eggs;
    this.drink = juice;
    }
  }


class Lunch {
  constructor(salad, broccoli, iced) {
  this.salad = salad;
  this.soup = broccoli;
  this.drink = iced

  }
}


class Dinner {
  constructor(salad, soup, entree, dessert) {
  this.salad = salad;
  this.soup = soup;
  this.entree = entree
  this._dessert = dessert

  }
}
